	private static int searchK(int[] array, int target) {
		int start = 0;
		int end = array.length - 1;

		while (start <= end) {
			int mid = start + (end - start) / 2;

			if (array[mid] == target) {
				return mid;
			} else if (array[mid] > target) {
				end = mid - 1;
			} else if (array[mid] < target) {
				start = mid + 1;
			}
		}
		return -1;

	}

	private static int solve(int[] nums) {

		int lo = 0, hi = nums.length - 1;
		while (lo < hi) {
			int mid = lo + (hi - lo) / 2;
			if (nums[mid] > nums[hi])
				lo = mid + 1;
			else if (nums[mid] < nums[hi])
				hi = mid;
			else
				hi--;
		}
		return lo;

	}

